# ── geocoding ──────────────────────────────────────────────────────────────────
_geocode_cache: dict[str, tuple[float, float, str]] = {}  # address -> (lat, lon, confidence)

def geocode_address(full_address: str, zip_code: str) -> tuple[float | None, float | None, str]:
    """
    Geocode via Nominatim. Returns (lat, lon, confidence).
    confidence: 'high' (full address match) | 'medium' (city/state/zip match) | 'zip_centroid'
    Respects OSM's 1-req/sec policy.
    """
    if full_address in _geocode_cache:
        return _geocode_cache[full_address]

    def _query(q, confidence):
        try:
            r = requests.get(
                NOMINATIM_URL,
                headers=NOMINATIM_HEADERS,
                params={"q": q, "format": "json", "limit": 1, "countrycodes": "us"},
                timeout=10,
            )
            time.sleep(1.1)  # OSM rate limit: 1 req/sec
            if r.status_code == 200:
                results = r.json()
                if results:
                    lat = float(results[0]["lat"])
                    lon = float(results[0]["lon"])
                    _geocode_cache[full_address] = (lat, lon, confidence)
                    return lat, lon, confidence
        except Exception:
            pass
        return None, None, None

    # Attempt 1: full address
    lat, lon, conf = _query(full_address, "high")
    if lat:
        return lat, lon, conf

    # Attempt 2: strip street number, keep city/state/zip
    parts = full_address.split(",")
    if len(parts) >= 3:
        fallback = ",".join(parts[1:]).strip()
        lat, lon, conf = _query(fallback, "medium")
        if lat:
            return lat, lon, conf

    # Attempt 3: zip centroid via pgeocode
    try:
        import pgeocode
        nomi = pgeocode.Nominatim("us")
        row = nomi.query_postal_code(zip_code)
        if row is not None and not __import__("math").isnan(row.latitude):
            result = (float(row.latitude), float(row.longitude), "zip_centroid")
            _geocode_cache[full_address] = result
            return result
    except Exception:
        pass

    _geocode_cache[full_address] = (None, None, "zip_centroid")
    return None, None, "zip_centroid"


# ── API fetch ──────────────────────────────────────────────────────────────────
def fetch_stores_for_zip(zip_code: str) -> tuple[list[dict], int]:
    """
    Call Albertsons API and return merged store list.
    Uses `instore.stores` as primary source (full address, up to 20 stores).
    Also unions `pickup.stores` by locationId.
    Ignores `delivery.stores` (no address field).
    """
    params = {
        "zipcode": zip_code,
        "size": 100,
        "radius": 200,
        "excludeBanners": "none",
        "includeNonMigratedStores": "true",
    }
    try:
        resp = requests.get(ABS_URL, headers=ABS_HEADERS, params=params, timeout=15)
        if resp.status_code != 200:
            return [], resp.status_code
        data = resp.json()
    except Exception as e:
        print(f"  ERROR fetching ZIP {zip_code}: {e}")
        return [], 0

    seen: dict[str, dict] = {}
    for key in ("instore", "pickup"):
        for store in data.get(key, {}).get("stores", []):
            loc_id = store.get("locationId")
            if loc_id and loc_id not in seen:
                seen[loc_id] = store

    return list(seen.values()), resp.status_code


def parse_store(store: dict) -> dict | None:
    """Parse an instore/pickup store dict into our DB schema."""
    loc_id = str(store.get("locationId", "")).strip()
    if not loc_id:
        return None

    addr = store.get("address", {})
    line1  = addr.get("line1",   "").strip()
    city   = addr.get("city",    "").strip()
    state  = addr.get("state",   "").strip()
    zipcode= addr.get("zipcode", "").strip()

    banner      = store.get("domainName", store.get("polarisBannerName", "")).strip()
    retailer_key= store.get("polarisBannerName", "").lower().strip()

    full_address = f"{line1}, {city}, {state} {zipcode}".strip(", ")

    return {
        "store_id":          loc_id,
        "retailer":          banner,
        "retailer_key":      retailer_key,
        "address":           line1,
        "zip_code":          zipcode,
        "full_address":      full_address,
        "latitude":          None,
        "longitude":         None,
        "geocode_source":    "zip_centroid",
        "geocode_confidence":"low",
    }


# ── DB helpers ─────────────────────────────────────────────────────────────────
def get_db_zips(supabase) -> set[str]:
    resp = supabase.table("store_locations").select("zip_code").execute()
    return {r["zip_code"] for r in resp.data if r.get("zip_code")}


def upsert_stores(supabase, stores: list[dict]) -> tuple[int, int]:
    """Upsert stores into store_locations. Returns (inserted, errors)."""
    now = datetime.now(timezone.utc).isoformat()
    inserted = 0
    errors   = 0

    for s in stores:
        row = {
            "retailer":           s["retailer"],
            "store_id":           s["store_id"],
            "latitude":           s.get("latitude"),
            "longitude":          s.get("longitude"),
            "address":            s.get("address"),
            "zip_code":           s.get("zip_code"),
            "full_address":       s.get("full_address"),
            "retailer_key":       s.get("retailer_key"),
            "geocode_source":     s.get("geocode_source", "zip_centroid"),
            "geocode_confidence": s.get("geocode_confidence", "low"),
            "geocoded_at":        now if s.get("latitude") else None,
            "updated_at":         now,
        }
        try:
            supabase.table("store_locations").upsert(
                row, on_conflict="retailer_key,store_id"
            ).execute()
            inserted += 1
        except Exception as e:
            print(f"  DB ERROR store {s['store_id']}: {e}")
            errors += 1

    return inserted, errors


